package linthost

import (
  "path/filepath"
  "strings"
  "testing"
)

// TestLSPExecuteCommandReturnsNullForNodeModulesTarget verifies dependency
// sources are not rewritten by LSP commands.
//
// The temp command workspace links `node_modules` back to the real project for
// config/module resolution. A command target under that linked tree would write
// through to dependencies, so the sidecar treats such targets as handled no-ops.
//
// 1. Seed a project with an included file under node_modules.
// 2. Execute `ttsc.lint.fixAll` for that dependency URI.
// 3. Assert the command returns JSON null.
// 4. Assert the dependency file on disk is unchanged.
func TestLSPExecuteCommandReturnsNullForNodeModulesTarget(t *testing.T) {
  root := t.TempDir()
  source := "var dependency = 1;\nJSON.stringify(dependency);\n"
  writeFile(t, filepath.Join(root, "tsconfig.json"), `{
  "compilerOptions": {
    "target": "ES2022",
    "module": "commonjs",
    "strict": true
  },
  "files": ["node_modules/demo/index.ts"]
}
`)
  seedLintRules(t, root, map[string]string{"no-var": "error"})
  file := filepath.Join(root, "node_modules", "demo", "index.ts")
  writeFile(t, file, source)
  uri := lintTestFileURI(t, file)

  argsJSON := `["` + uri + `"]`
  code, stdout, stderr := captureCommandOutput(t, func() int {
    return run([]string{
      "lsp-execute-command",
      "--cwd", root,
      "--plugins-json", lintManifest(t),
      "--command", commandLintFixAll,
      "--arguments-json", argsJSON,
    })
  })
  if code != 0 || stderr != "" {
    t.Fatalf("lsp-execute-command mismatch: code=%d stdout=%q stderr=%q", code, stdout, stderr)
  }
  if strings.TrimSpace(stdout) != "null" {
    t.Fatalf("expected node_modules command target to return null, got %q", stdout)
  }
  assertFileText(t, file, source)
}
