package linthost

import (
  "path/filepath"
  "testing"
)

// TestLSPCodeActionsReturnsEmptyForNodeModulesTarget verifies dependency files
// do not advertise commands that intentionally no-op.
//
// ExecuteCommand targets under `node_modules` return JSON null to avoid writing
// through the linked dependency tree. Code actions should make the same
// boundary decision so VSCode does not show a fix-all button that cannot apply.
//
// 1. Seed a project with an included dependency source file.
// 2. Enable a fixable lint rule.
// 3. Request `source.fixAll.ttsc` code actions for the dependency URI.
// 4. Assert the action list is empty.
func TestLSPCodeActionsReturnsEmptyForNodeModulesTarget(t *testing.T) {
  root := t.TempDir()
  writeFile(t, filepath.Join(root, "tsconfig.json"), `{
  "compilerOptions": {
    "target": "ES2022",
    "module": "commonjs",
    "strict": true
  },
  "files": ["node_modules/demo/index.ts"]
}
`)
  seedLintRules(t, root, map[string]string{"no-var": "error"})
  file := filepath.Join(root, "node_modules", "demo", "index.ts")
  writeFile(t, file, "var dependency = 1;\nJSON.stringify(dependency);\n")
  uri := lintTestFileURI(t, file)

  actions := runLSPCodeActionsForTest(t, root, uri, `{"only":["source.fixAll.ttsc"]}`)
  if len(actions) != 0 {
    t.Fatalf("node_modules target advertised code actions: %#v", actions)
  }
}
