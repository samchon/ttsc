package main

import (
  "os"
  "path/filepath"
  "strings"
  "testing"
)

// TestNodeConfigLoaderEnvPrependsNearestNodeModules verifies NODE_PATH loader metadata.
//
// JavaScript and TypeScript config loading run in subprocesses. The loader
// environment should prepend the nearest project node_modules path while
// preserving an already configured NODE_PATH value.
//
// This scenario exercises nodeConfigLoaderEnv, findNearestNodeModules, and the
// setEnv replacement branch through the same path used by config subprocesses.
//
// 1. Create a project tree with a node_modules directory above the config file.
// 2. Seed NODE_PATH with an existing value.
// 3. Assert the generated environment prepends the nearest node_modules path.
func TestNodeConfigLoaderEnvPrependsNearestNodeModules(t *testing.T) {
  root := t.TempDir()
  nodeModules := filepath.Join(root, "node_modules")
  if err := os.MkdirAll(nodeModules, 0o755); err != nil {
    t.Fatalf("MkdirAll: %v", err)
  }
  config := filepath.Join(root, "packages", "app", "lint.config.js")
  writeFile(t, config, "export default {};")
  t.Setenv("NODE_PATH", "already-present")

  env := nodeConfigLoaderEnv(config)
  want := "NODE_PATH=" + nodeModules + string(os.PathListSeparator) + "already-present"
  for _, entry := range env {
    if entry == want {
      return
    }
  }
  matches := []string{}
  for _, entry := range env {
    if strings.HasPrefix(entry, "NODE_PATH=") {
      matches = append(matches, entry)
    }
  }
  t.Fatalf("NODE_PATH overlay %q not found in env entries containing NODE_PATH: %v", want, matches)
}
