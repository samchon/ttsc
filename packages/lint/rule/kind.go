// Kind constants re-exported as type-alias values so contributors can
// reference AST node kinds via the `rule` package without importing
// `shim/ast` directly. Adding or renaming a kind upstream requires a
// matching change here; the host build catches missing aliases as
// compile-time errors against contributors that depend on them.

