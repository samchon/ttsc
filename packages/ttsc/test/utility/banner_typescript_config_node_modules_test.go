package ttsc_test

import (
  "encoding/json"
  "os"
  "path/filepath"
  "strings"
  "testing"

  "github.com/samchon/ttsc/packages/ttsc/utility"
)

// TestUtilityBannerTypeScriptConfigUsesNearestNodeModules verifies TypeScript
// banner config loading keeps package resolution rooted at the config project.
//
// TypeScript banner configs run through a temporary loader project. That loader
// must still see the nearest project `node_modules`, otherwise real configs
// that import small helper packages fail even though the project layout is
// valid.
//
// 1. Create a project with a TypeScript banner config importing a local package.
// 2. Point the loader at the local ttsx launcher and tsgo binary.
// 3. Assert the imported package text is injected into transformed source.
func TestUtilityBannerTypeScriptConfigUsesNearestNodeModules(t *testing.T) {
  root := t.TempDir()

  // Scenario setup: this follows the same slow loader path as production
  // TypeScript config files, including node_modules linking into temp space.
  rootPackage := packageRoot(t)
  tsgo := filepath.Join(rootPackage, "node_modules", ".bin", "tsgo")
  if _, err := os.Stat(tsgo); err != nil {
    t.Skip("local tsgo binary is required for TypeScript banner config loading")
  }
  ttsx := filepath.Join(rootPackage, "lib", "launcher", "ttsx.js")
  if _, err := os.Stat(ttsx); err != nil {
    t.Skip("local ttsx launcher is required for TypeScript banner config loading")
  }
  t.Setenv("TTSC_TTSX_BINARY", ttsx)
  t.Setenv("TTSC_TSGO_BINARY", tsgo)
  t.Setenv("NODE_PATH", filepath.Join(root, "existing-node-path"))

  writeProjectFile(t, root, "tsconfig.json", `{
  "compilerOptions": {
    "module": "esnext",
    "target": "es2022"
  },
  "files": ["index.ts"]
}
`)
  writeProjectFile(t, root, "index.ts", `export const value = 1;
`)
  writeProjectFile(t, root, "node_modules/nearest-banner/package.json", `{
  "name": "nearest-banner",
  "type": "module",
  "exports": {
    ".": {
      "types": "./index.d.ts",
      "import": "./index.js",
      "default": "./index.js"
    }
  }
}
`)
  writeProjectFile(t, root, "node_modules/nearest-banner/index.d.ts", `export declare const text: string;
`)
  writeProjectFile(t, root, "node_modules/nearest-banner/index.js", `export const text = "Nearest Node Modules Banner";
`)
  writeProjectFile(t, root, "banner.config.ts", `import { text } from "nearest-banner";

export default { text };
`)

  // Transform assertion: the banner text can only appear if the temporary
  // loader resolves the package through the project node_modules.
  code, out, errOut := captureUtilityOutput(t, func() int {
    return utility.RunTransform([]string{
      "--cwd", root,
      "--plugins-json", `[{"name":"@ttsc/banner"}]`,
    })
  })
  if code != 0 {
    t.Fatalf("RunTransform failed: code=%d stdout=%q stderr=%q", code, out, errOut)
  }
  var transformed utilityTransformResult
  if err := json.Unmarshal([]byte(strings.TrimSpace(out)), &transformed); err != nil {
    t.Fatalf("RunTransform JSON decode failed: %v\nstdout=%s\nstderr=%s", err, out, errOut)
  }
  if !strings.Contains(transformed.TypeScript["index.ts"], "Nearest Node Modules Banner") {
    t.Fatalf("nearest node_modules banner was not injected: %#v", transformed.TypeScript)
  }
}
